class Solution {
  public:
    int romanToDecimal(string &str) {
        // code here
        unordered_map<char,int> map = {{'I',1},{'V',5},{'X',10},{'L',50},{'C',100},{'D',500},{'M',1000}};
        int res = map[str[str.size()-1]];
        for(int i = str.size()-2;i>=0;i--) {
            if(map[str[i]] < map[str[i+1]]) res-=map[str[i]];
            else res+=map[str[i]];
        }
        return res;
    }
};

