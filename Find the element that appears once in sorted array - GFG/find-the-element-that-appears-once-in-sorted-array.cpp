class Solution
{
  public:
    int findOnce(int a[], int n)
    {
        //code here.
        if(n==0) return a[0];
        
        for(int i=0;i<n-1;i+=2) {
            if(a[i]!=a[i+1]) return a[i];
        }
        return a[n-1];
    }
};

